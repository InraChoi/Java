package com.example.day02;

public class Variables {

	public static void main(String[] args) {
		// 단계1
		// 정수형 변수 intVar을 선언
		int intVar;
		// intVar에 값 10을 대입
		intVar = 10;	// not 'equal to' but 'assign to'
		// intVar값을 출력한다.
		System.out.printf("%d",intVar);
		
		//단계2
		// 변수명명 규칙과 camel notation을 설명한다.
		// 문자로 시작하고... 문자와 숫자를 섞어서 변수명을 만든다.
		// 변수명은 의미를 부여해서..
		
		int korScore = 100;
		// camel notation
		
		
		

	}

}
